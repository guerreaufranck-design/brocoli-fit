window.I18N = {
  current: 'fr',

  init() {
    const saved = localStorage.getItem('brocoliLang') || 'fr';
    this.setLang(saved, false);
    this._initLangSelector();
  },

  setLang(lang, save = true) {
    if (!TRANSLATIONS[lang]) lang = 'fr';
    this.current = lang;
    if (save) localStorage.setItem('brocoliLang', lang);
    document.documentElement.lang = lang;
    this.applyAll();
    this._updateSelectors(lang);
  },

  t(key) {
    return TRANSLATIONS[this.current]?.[key] || TRANSLATIONS['fr']?.[key] || '';
  },

  applyAll() {
    // data-i18n attributes
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = this.t(key);
      if (val) el.textContent = val;
    });
    // data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const val = this.t(key);
      if (val) el.placeholder = val;
    });
  },

  getCulturalProfile() {
    return CULTURAL_PROFILES[this.current] || CULTURAL_PROFILES['fr'];
  },

  _initLangSelector() {
    const btn      = document.getElementById('langBtn');
    const dropdown = document.getElementById('langDropdown');
    const selector = document.getElementById('langSelector');
    const options  = document.querySelectorAll('.lang-option');

    if (!btn || !dropdown) return;

    const close = () => {
      dropdown.classList.remove('open');
      selector?.classList.remove('open');
    };

    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      const isOpen = dropdown.classList.contains('open');
      if (isOpen) {
        close();
      } else {
        dropdown.classList.add('open');
        selector?.classList.add('open');
        // Listen for outside click only after opening (next tick avoids self-close)
        setTimeout(() => document.addEventListener('click', close, { once: true }), 0);
      }
    });

    options.forEach(opt => {
      opt.addEventListener('click', (e) => {
        e.stopPropagation();
        I18N.setLang(opt.dataset.lang);
        close();
        options.forEach(o => o.classList.remove('active'));
        opt.classList.add('active');
      });
    });
  },

  _updateSelectors(lang) {
    const flagEl = document.getElementById('currentFlag');
    const langEl = document.getElementById('currentLang');
    const flags  = { fr:'🇫🇷', en:'🇬🇧', de:'🇩🇪', it:'🇮🇹' };
    if (flagEl) flagEl.textContent = flags[lang] || '🌍';
    if (langEl) langEl.textContent = lang.toUpperCase();
    document.querySelectorAll('.lang-option').forEach(o => {
      o.classList.toggle('active', o.dataset.lang === lang);
    });
  }
};

// Expose shorthand
